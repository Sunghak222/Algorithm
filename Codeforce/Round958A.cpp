void solve()
{
    int n,k;
    int ans = 0;
    cin >> n >> k;
    if (n == 1)
    {
        cout << 0 << '\n';
        return;
    }
    while (true)
    {
        n -= (k-1);
        ans++;
        if (n <= 1) break;
    }
    cout << ans << '\n';
}

